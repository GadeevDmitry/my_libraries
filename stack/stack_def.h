#ifndef STACK_DEF_H
#define STACK_DEF_H

//--------------------------------------------------------------------------------------------------------------------------------
// ctor
//--------------------------------------------------------------------------------------------------------------------------------

#define stack_ctor(stk, el_size, ...) _stack_ctor(__FILE__, __PRETTY_FUNCTION__, __LINE__, stk, el_size, ##__VA_ARGS__)
#define stack_new(      el_size, ...) _stack_new (__FILE__, __PRETTY_FUNCTION__, __LINE__,      el_size, ##__VA_ARGS__)

//--------------------------------------------------------------------------------------------------------------------------------
// dtor
//--------------------------------------------------------------------------------------------------------------------------------

#define stack_dtor(stk) _stack_dtor(__FILE__, __PRETTY_FUNCTION__, __LINE__, stk)

//--------------------------------------------------------------------------------------------------------------------------------
// push pop
//--------------------------------------------------------------------------------------------------------------------------------

#define stack_push(stk, data) _stack_push(__FILE__, __PRETTY_FUNCTION__, __LINE__, stk,          data)
#define stack_pop( stk,  ...) _stack_pop (__FILE__, __PRETTY_FUNCTION__, __LINE__, stk, ##__VA_ARGS__)

//--------------------------------------------------------------------------------------------------------------------------------
// other
//--------------------------------------------------------------------------------------------------------------------------------

#define stack_front(   stk, data) _stack_front   (__FILE__, __PRETTY_FUNCTION__, __LINE__, stk, data)
#define stack_is_empty(stk      ) _stack_is_empty(__FILE__, __PRETTY_FUNCTION__, __LINE__, stk      )

//--------------------------------------------------------------------------------------------------------------------------------
// stack dump
//--------------------------------------------------------------------------------------------------------------------------------

#define stack_dump(stk) _stack_dump(__FILE__, __PRETTY_FUNCTION__, __LINE__, stk)

#endif // STACK_DEF_H